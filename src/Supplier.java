    public void allSupplierRecord(){
        try {
            con = dbConnection.getCon();
            st = con.createStatement();
            rs = st.executeQuery("SELECT * from supplier");
            //allEmpRecordTable.setAutoResizeMode(allEmpRecordTable.AUTO_RESIZE_OFF);
           // Set all the content to table
           DefaultTableModel table = (DefaultTableModel) allSupplierTable.getModel();
            table.setRowCount(0);
            while(rs.next()){
                Object record[] = {
                    rs.getString("id"),
                    rs.getString("name"),
                    rs.getString("phone"),
                    rs.getString("address"),
                    rs.getString("email")
                };
                table.addRow(record);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null,"Connection failed: \n"+ e.getMessage());
        }finally{
            try {
                st.close();
                //ps.close();
                rs.close();
            } catch (SQLException e) {
            }
        }
    }
