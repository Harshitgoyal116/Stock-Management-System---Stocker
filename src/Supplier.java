    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // Update Button in manage Supplier Pannel
        String id = updateSupplierIdComboBox.getSelectedItem().toString();
        String name = updateSupplierNameText.getText();
        String phone = updateSupplierPhoneText.getText();
        String address = updateSupplierAddressText.getText();
        String email = updateSupplierEmailText.getText();
        try {
            con = dbConnection.getCon();
            ps = con.prepareStatement("update supplier set name=?, phone=?, address=?, email=? where id='"+ id +"'");
            ps.setString(1, name);
            ps.setString(2, phone);
            ps.setString(3, address);
            ps.setString(4, email);

            int response = JOptionPane.showConfirmDialog(null, "Do you want to sure to update?", "select", JOptionPane.YES_NO_OPTION);
            if(response == JOptionPane.YES_OPTION){

                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Successfully updated"); 
            }
            updateReset();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null,"Connection failed: \n"+ e.getMessage());
        }finally{
            try {
                ps.close();
            } catch (SQLException e) {
            }
        }
    }//GEN-LAST:event_updateBtnActionPerformed

