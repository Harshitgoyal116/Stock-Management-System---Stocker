    private void addSupplierAddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addSupplierAddBtnActionPerformed
        // Add Supplier Add Button
        String id = addSupplierIdText.getText();
        String name = addSupplierNameText.getText();
        String phone = addSupplierPhoneText.getText();
        String address = addSupplierAddressText.getText();
        String email = addSupplierEmailText.getText();
        try {
            con = dbConnection.getCon();
            ps = con.prepareStatement("insert into supplier values(?,?,?,?,?)");
            ps.setString(1, id);
            ps.setString(2, name);
            ps.setString(3, phone);
            ps.setString(4, address);
            ps.setString(5, email);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Sucessfully saved");
            addSupplierReset();
            addSupplierIdText.requestFocus();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Connection failed: \n" +e.getMessage());
        }finally{
            try {
                ps.close();
            } catch (SQLException e) {
            }
        }
    }//GEN-LAST:event_addSupplierAddBtnActionPerformed

    private void allSupplierIdTextKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_allSupplierIdTextKeyReleased
        // All Supplier Filter By ID
        if(allSupplierIdText.getText().equals("")){ 
            allSupplierRecord();
        }
        else
        try {
            String id = allSupplierIdText.getText();
            con = dbConnection.getCon();
            st = con.createStatement();
            rs = st.executeQuery("SELECT * from supplier where id ='"+id+"'");
            //allEmpRecordTable.setAutoResizeMode(allEmpRecordTable.AUTO_RESIZE_OFF); // to make  down scroll bar in table 
            // Set all the content to table
            DefaultTableModel table = (DefaultTableModel) allSupplierTable.getModel();
            table.setRowCount(0);
            while(rs.next()){
                Object record[] = {
                    rs.getString("id"),
                    rs.getString("name"),
                    rs.getString("phone"),
                    rs.getString("address"),
                    rs.getString("email")
                };
                table.addRow(record);
            }
        } catch (SQLException e) {
           
            JOptionPane.showMessageDialog(null, "Connection failed: \n"+e.getMessage());
        }finally{
            try {
                st.close();
                rs.close();
            } catch (SQLException e) {
            }
        }
    }//GEN-LAST:event_allSupplierIdTextKeyReleased

