    // Fetch all Suppliers from data base and set in the como box field
    public void fetchSupplier(){
        try{
            con = dbConnection.getCon();
            String sql = "Select * from supplier";
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            while(rs.next()){
                updateSupplierIdComboBox.addItem(rs.getString("id"));
                deleteSupplierIdComboBox.addItem(rs.getString("id"));
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(rootPane,"Coonection failed: \n"+ e.getMessage());
        }finally{
            try {
                ps.close();
                rs.close();
            } catch (SQLException e) {
            }
        }
    }
    
