    // save button function
    public void save(){
        String name = nameText.getText();
        String username = usernameText.getText();
        String password = passwordText.getText();
        String security = questionText.getText();
        String answer = answerText.getText();
        try {
            con = dbConnection.getCon();
            ps = con.prepareStatement("update login set Username=?, Password=?, Name=?, Security=?, Answer=? where sno=1");
            ps.setString(1, username);
            ps.setString(2, password);
            ps.setString(3, name);
            ps.setString(4, security);
            ps.setString(5, answer);
            
            //upadte query
            ps.executeUpdate();
            JOptionPane.showMessageDialog(rootPane, "Successfully updated");
            // close and re call the same page
            setVisible(false);
            new Setting().setVisible(true);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(rootPane,"Connection failed: \n"+ e.getMessage());
        }finally{
            try {
                ps.close();
            } catch (SQLException e) {
            }
        }
    }
    
