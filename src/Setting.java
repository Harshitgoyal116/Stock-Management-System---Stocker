    // fetch all data form database using login table
    public void fetch(){
        try{
            con = dbConnection.getCon();
            String sql = "select * from login";
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            
            if(rs.next()){                
                // Name: database name
                String name = rs.getString(4);
                nameText.setText(name);
                
                // Username: database username
                String username = rs.getString(2);
                usernameText.setText(username);
                
                String password = rs.getString(3);
                passwordText.setText(password);
                
                // security question: database security question
                String security = rs.getString(5);
                questionText.setText(security);
                
                String ans = rs.getString(6);
                answerText.setText(ans);
                
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this,"Connection failed: \n" + e.getMessage());   
        } finally{
           try{
                rs.close();
                ps.close();
           }catch(SQLException e){
               
           }
       }
    }

